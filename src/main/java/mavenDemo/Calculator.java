package mavenDemo;

public class Calculator
{
   public Calculator()
   {
   }
   
   public int summe(int summand1, int summand2)
   {
      return summand1 + summand2;
   }

   public int differenz(int minuend, int subtrahend)
   {
      return minuend - subtrahend;
   }
}
